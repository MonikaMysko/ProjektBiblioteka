package model;

public class Author {

    private String name;
    private String AuthorUrl;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAuthorUrl() {
        return AuthorUrl;
    }

    public void setAuthorUrl(String authorUrl) {
        AuthorUrl = authorUrl;
    }
}
