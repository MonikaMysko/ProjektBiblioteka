public class Epoch {

    private String EpochName;
    private String EpochUrl;

    public String getEpochName() {
        return EpochName;
    }

    public void setEpochName(String epochName) {
        EpochName = epochName;
    }

    public String getEpochUrl() {
        return EpochUrl;
    }

    public void setEpochUrl(String epochUrl) {
        EpochUrl = epochUrl;
    }
}
